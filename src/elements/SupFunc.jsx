// утилита для генерации slug из кириллицы/латиницы
export const slugify = (text) => {
    const map = {
        а: "a", б: "b", в: "v", г: "g", д: "d", е: "e", ё: "e", ж: "zh", з: "z",
        и: "i", й: "y", к: "k", л: "l", м: "m", н: "n", о: "o", п: "p", р: "r",
        с: "s", т: "t", у: "u", ф: "f", х: "h", ц: "ts", ч: "ch", ш: "sh", щ: "sch",
        ъ: "", ы: "y", ь: "", э: "e", ю: "yu", я: "ya",
    };

    return text
        .toString()
        .toLowerCase()
        .split("")
        .map((char) => map[char] || char) // транслит или оставить символ
        .join("")
        .replace(/\s+/g, "-")      // пробелы → дефисы
        .replace(/[^a-z0-9\-]/g, "") // убираем всё кроме латиницы, цифр и дефисов
        .replace(/\-+/g, "-")      // несколько дефисов → один
        .replace(/^-+|-+$/g, "");  // убрать дефисы по краям
};